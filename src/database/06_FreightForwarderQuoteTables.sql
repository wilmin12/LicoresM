-- Table 2: FF_OCEAN_FREIGHT_HEADER
-- One header per quote per forwarder.
CREATE TABLE dbo.FF_OCEAN_FREIGHT_HEADER (
    FQOH_Id             INT             NOT NULL IDENTITY(1,1),
    FQOH_FORWARDER      NVARCHAR(10)    NOT NULL,
    FQOH_QUOTENUMBER    NVARCHAR(10)    NOT NULL,
    FQOH_START_DATE     DATE            NULL,
    FQOH_END_DATE       DATE            NULL,
    FQOH_REMARKS        NVARCHAR(200)   NULL,

    CONSTRAINT PK_FF_OCEAN_FREIGHT_HEADER   PRIMARY KEY CLUSTERED (FQOH_Id),
    CONSTRAINT UQ_FQOH_ForwarderQuote       UNIQUE (FQOH_FORWARDER, FQOH_QUOTENUMBER)
);
GO

CREATE INDEX IX_FQOH_Forwarder ON dbo.FF_OCEAN_FREIGHT_HEADER (FQOH_FORWARDER);
GO

-- Table 3: FF_OCEAN_FREIGHT_PORT
-- One row per port within a quote.
CREATE TABLE dbo.FF_OCEAN_FREIGHT_PORT (
    FQOP_Id             INT             NOT NULL IDENTITY(1,1),
    FQOP_Header_Id      INT             NOT NULL,
    FQOP_FORWARDER      NVARCHAR(10)    NOT NULL,
    FQOP_QUOTENUMBER    NVARCHAR(10)    NOT NULL,
    FQOP_PORT           NVARCHAR(10)    NOT NULL,
    FQOP_REMARKS        NVARCHAR(200)   NULL,

    CONSTRAINT PK_FF_OCEAN_FREIGHT_PORT     PRIMARY KEY CLUSTERED (FQOP_Id),
    CONSTRAINT FK_FQOP_Header               FOREIGN KEY (FQOP_Header_Id)
        REFERENCES dbo.FF_OCEAN_FREIGHT_HEADER (FQOH_Id) ON DELETE CASCADE
);
GO

CREATE INDEX IX_FQOP_Header ON dbo.FF_OCEAN_FREIGHT_PORT (FQOP_Header_Id);
GO

