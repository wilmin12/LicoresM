-- ============================================================
-- Table 1: FREIGHT_FORWARDERS
-- Master list of freight forwarding companies.
-- ============================================================
CREATE TABLE dbo.FREIGHT_FORWARDERS (
    FF_Id           INT             NOT NULL IDENTITY(1,1),
    FF_CODE         NVARCHAR(10)    NOT NULL,
    FF_NAME         NVARCHAR(50)    NOT NULL,
    FF_ADDRESS_1    NVARCHAR(100)   NULL,
    FF_ADDRESS_2    NVARCHAR(100)   NULL,
    FF_CITY         NVARCHAR(30)    NULL,
    FF_COUNTRY      NVARCHAR(3)     NULL,
    FF_PHONE_1      NVARCHAR(20)    NULL,
    FF_PHONE_2      NVARCHAR(20)    NULL,
    FF_EMAIL        NVARCHAR(100)   NULL,
    FF_CONTACT      NVARCHAR(50)    NULL,
    FF_CURRENCY     NVARCHAR(3)     NULL,
    IsActive        BIT             NOT NULL DEFAULT 1,
    CreatedAt       DATETIME2       NOT NULL DEFAULT GETDATE(),

    CONSTRAINT PK_FREIGHT_FORWARDERS        PRIMARY KEY CLUSTERED (FF_Id),
    CONSTRAINT UQ_FREIGHT_FORWARDERS_CODE   UNIQUE (FF_CODE)
);
GO

CREATE INDEX IX_FF_Name ON dbo.FREIGHT_FORWARDERS (FF_NAME);
GO

